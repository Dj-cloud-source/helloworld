#include<stdio.h>
#include<iostream>
#include<stdlib.h>
#include<time.h>
int main()
{
	srand(time(0));
	
	int number=rand()%100+1;
	int count=0;
	int a=0;
	
	printf("我已经想好一个1到100之间的数了。\n");
	
	do{
		
		printf("请猜这个数：");
		scanf("%d",&a);
		count++  ;
		
		if  (a>number){
		   printf("你猜的数大了，继续猜。\n");
		    
		} else if (a<number){
			printf("你猜的数小了，继续猜。\n");
			
		}
		
		
		
		
		
		
		
		
		
		
		
	} while  (a != number);
	
	printf("太好了，你用了%d次就猜到答案。\n",count);
	
	
	
	
	
	
	
	
	
	
	
	
	
	system("pause");
	return 0;
}
